package diyarenny;

public class Grade {

	//score variable
	int score;

	//empty constructor 
	public Grade() {

	}

	//constructor with score parameter
	public Grade(int score) {
		super();
		this.score = score;
	}

	//getter method for score
	public int getScore() {
		return score;
	}

	//setter method for score
	public void setScore(int score) {
		this.score = score;
	}

}
